          <p>La salvación, es personal, las cosas eternas siempre están relacionadas, con cosas personales, es el tiempo 
             de reflexión; ¿que está impidiendo que yo pueda ser arrebatado? ¿Qué decadencia moral?, ¿qué cosa pecaminosa 
             está impidiendo en mi vida?, a pesar que estoy recibiendo muchos mensajes, pero no pueden romper ni destruir,
