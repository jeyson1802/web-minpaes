          <p>Sangre y carne no heredaran el reino de Dios, como menciona el siguiente versículo:</p>

          <blockquote class="blockquote background-lighter-gray text-center">
            <h3 class="quote">"Pero esto digo, hermanos: que la carne y la sangre no pueden heredar el reino de Dios, ni la 
            corrupción hereda la incorrupción."</h3>
            <span class="citation">1 Corintios 15:50</span>
          </blockquote>

          <p>Sabemos que es un tiempo tan difícil que estamos viviendo aun los que no conocen de Dios saben, que algo está 
             pasando en el mundo entero, ven como algo inusual; como ha aumentado las violaciones, homosexualismos, escándalos
             en las iglesias, toda esta decadencia nos hace llevar; de que la venida de Jesucristo es inminente , y ¿estamos 
             ya listos para irnos? </p>
          <p>Es el tiempo en donde se separara la cizaña del trigo; una iglesia prudente, llena de aceite, y una iglesia insensata 
             que no supo preparar sus vestidos, que no supo llenarse del aceite, que no se arrepintió que recibió muchos mensajes, 
             estando aun en iglesias, pero no eran los mensajes con el poder suficiente para limpiarlo.
             El tiempo de la verdad ha llegado, Jesucristo está viniendo por una iglesia sin mancha y sin arruga; La venida de
             Jesucristo es inminente, acabamos de ver  hace más de una semana la SHEKINA, la gloria de Dios, la visitación de Dios, 
             mostrada al mundo entero y esto sucedió en Lima – Perú; muchos desconocen este acontecimiento histórico, es una
             advertencia de que el Mesías ¡Ya! está viniendo. La escalera ya ha descendido, una vez que ha sido vista la gloria, 
             ahora estamos en los descuentos ¡Ya! para en cualquier momento ser arrebatados, como lo menciona el siguiente versículo: </p>

          <blockquote class="blockquote background-lighter-gray text-center">
            <h3 class="quote">"Porque es necesario que esto corruptible se vista de incorrupción, y esto mortal se vista de
             inmortalidad. Y cuando esto corruptible se haya vestido de incorrupción, y esto mortal se haya vestido de 
             inmortalidad, entonces se cumplirá la palabra que está escrita: Sorbida es la muerte en victoria."</h3>
            <span class="citation">1 Corintios 15: 53-54</span>
          </blockquote>

          <p>Para que la inmortalidad se levante en mi vida tiene que salir toda decadencia moral y pecaminosa, y solo el 
             mensaje de Jesús, tiene el poder para destruir y arruinar. La vieja cruz, una vez limpiada desde los pulpitos, 
             para desde ahí predicar el mensaje santo, el mensaje que tiene el poder, para santificar al pueblo, el mensaje 
             del Mesías, es que procedamos a arrepentimiento, que procedamos a aparatarnos y separarnos para él. No es el
             tiempo de depender de las iglesias, es el tiempo de estar en una iglesia, pero dentro de ella 
             apartado y separado de una manera personal con Dios, las iglesias no salvamos, los pastores no  salvamos, 
             solo Cristo salva, solo Cristo sana, libera, solo Jesucristo tiene el poder de cambiar lo mortalidad en 
             inmortalidad, lo perecible en lo imperecible, lo corrupto en incorruptible, solo él tiene el mensaje exacto 
             para preparar nuestras vestiduras para su pronta venida. Iglesia despierta, reacciona, aléjate de la aprobación
             de los hombres, sepárate para Dios, porque tu rey está viniendo, y viene por una iglesia santa, una iglesia que 
             se empieza a vestir de lino fino, una iglesia que entendió y discernió los tiempos, el tiempo se ha acabado, 
             EL MESIAS ESTA YA VINIENDO, que te encuentre preparado y lista, limpia con lino fino y resplandeciente, viene 
             por su novia; la novia ataviada y limpia en santidad.  </p>
          
          <p>El Mesías ha anunciado ya su gloria desde Lima - Perú para las naciones de la tierra.</p>
          
